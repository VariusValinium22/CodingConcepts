// .indexOf()
// non-mutating Method
// find the FIRST indexOf an array item
let names = ["Danny", "Joe", "Sarah", "Molly", "Joe"];

let index = names.indexOf('Joe');
console.log(index); // 1

// what is a real-world need to know the index of a specified array item?
let index2 = names.lastIndexOf('Joe');
console.log(index2); // 4
